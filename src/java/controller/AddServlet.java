    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String name = request.getParameter("name");
        String image = request.getParameter("image");
        String price_raw = request.getParameter("price");
        String title = request.getParameter("title");
        String description = request.getParameter("description");
        String quantiry_raw = request.getParameter("quantity");
        String category_raw = request.getParameter("category");
        int price, category , quantity;
        try {
            price = Integer.parseInt(price_raw);
            category = Integer.parseInt(category_raw);
            quantity = Integer.parseInt(quantiry_raw);
            HttpSession session = request.getSession();
            Account a = (Account) session.getAttribute("account");
            ProductDAO pd = new ProductDAO();
            
            if (a != null) {
                int sell_id = a.getUid();
                
                pd.AddProduct(name, image, price, title, description, quantity, category, sell_id);
                response.sendRedirect("manageP");
            }
        } catch (NumberFormatException e) {
            System.out.println(e);
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

