    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String pid_raw = request.getParameter("pid");
        int pid;
        ProductDAO pd = new ProductDAO();
        try {
            pid = Integer.parseInt(pid_raw);
            Product p = pd.GetProductByPid(pid);
            request.setAttribute("update", p);
            request.getRequestDispatcher("update.jsp").forward(request, response);
        } catch (NumberFormatException e) {
            System.out.println(e);
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.setCharacterEncoding("UTF-8");
        //lay du lieu tu form
        String pid_raw = request.getParameter("productID");
        String name = request.getParameter("productName");
        String image = request.getParameter("productImage");
        String productPrice_raw = request.getParameter("productPrice");
        String productTitle = request.getParameter("productTitle");
        String productDescription = request.getParameter("productDescription");
        
        int productPrice,pid;
        ProductDAO pd = new ProductDAO();

        try {
            pid = Integer.parseInt(pid_raw);
            HttpSession session = request.getSession();
            Account a = (Account) session.getAttribute("account");
            if (a != null) {
                productPrice = Integer.parseInt(productPrice_raw);
                pd.UpdateProduct(pid, name, image, productPrice, productTitle,productDescription);
                response.sendRedirect("manageP");
            }

        } catch (NumberFormatException e) {
            System.out.println(e);
        }
    }

